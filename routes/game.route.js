const express = require('express');
const router = express.Router();

const authorization = require('../middlewares/authorization.middleware');
const gameController = require('../controllers/game.controller');

/////////////////////////////////////////////////////////////////////////////////////////
// Get & Post route for game creation
/////////////////////////////////////////////////////////////////////////////////////////
router.get('/new', gameController.createGame);
router.post(
  '/new',
  gameController.validate('createGame'),
  gameController.createGame
);

/////////////////////////////////////////////////////////////////////////////////////////
// Get routes for joining or unjoining a game, ideally it should be post routes
/////////////////////////////////////////////////////////////////////////////////////////
router.get('/:gameId/join', gameController.joinSelfGame);
router.get('/:gameId/remove', gameController.removeSelfGame);

/////////////////////////////////////////////////////////////////////////////////////////
// Get & Post route to edit a game
/////////////////////////////////////////////////////////////////////////////////////////
router.get(
  '/:gameId/edit',
  authorization.canEditGame,
  gameController.updateGame
);
router.post(
  '/:gameId/edit',
  authorization.canEditGame,
  gameController.validate('updateGame'),
  gameController.updateGame
);

/////////////////////////////////////////////////////////////////////////////////////////
// Get Game detail
/////////////////////////////////////////////////////////////////////////////////////////
router.get('/:gameId', gameController.findGame);

/////////////////////////////////////////////////////////////////////////////////////////
// Get All Games - i.e. the homepage
/////////////////////////////////////////////////////////////////////////////////////////
router.get('/', gameController.findGames);

/////////////////////////////////////////////////////////////////////////////////////////
// Delete a game
/////////////////////////////////////////////////////////////////////////////////////////
router.delete('/:gameId', authorization.canEditGame, gameController.deleteGame);

module.exports = router;
